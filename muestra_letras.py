"""
ENUNCIADO: La función muestra_letras debe imprimir cada letra de una palabra 
en una línea separada. Complete los espacios en blanco para que eso suceda.
"""
def muestra_letras(palabra):
	for letra in palabra:
		print(letra)

muestra_letras("Hola")
